package com.lbs.client;

public class GPSEntity {
	
	private double x;//经度
	private double y;//纬度
	private double h;//高度
	private double s;//速度
	
	public double getX() {
		return x;
	}
	public void setX(double x) {
		this.x = x;
	}
	public double getY() {
		return y;
	}
	public void setY(double y) {
		this.y = y;
	}
	public double getH() {
		return h;
	}
	public void setH(double h) {
		this.h = h;
	}
	public double getS() {
		return s;
	}
	public void setS(double s) {
		this.s = s;
	}
}
